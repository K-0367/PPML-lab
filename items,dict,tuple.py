my_list = [10, 20, 30, 40]
print("List elements:")
for x in my_list:
    print(x)
my_tuple = (1, 2, 3, 9)
print("\nTuple elements:")
for item in my_tuple:
    print(item)
my_dict = {'a': 100, 'b': 200, 'c': 300}
print("\nDictionary elements:")
for key, value in my_dict.items():
    print(key, ":", value)
my_set = {5, 25, 15, 10}
print("\nSet elements:")
for item in my_set:
    print(item)
